

# Basic arithmetic
print(3 + 4)        # Addition → 7
print(10 - 2)       # Subtraction → 8
print(5 * 3)        # Multiplication → 15
print(8 / 2)        # Division → 4.0
print(2 ** 3)       # Exponentiation → 8
print("")
# Modulus (Remainder)
print(10 % 3)       # 1
print("")
# Order of operations (PEMDAS)
print(3 + 4 * 2)    # 3 + 8 → 11
print((3 + 4) * 2)  # 7 * 2 → 14
print("")
# Negative numbers and absolute value
my_num = -5
print(abs(my_num))  # → 5
print("")
# Power and square root
print(pow(2, 3))    # → 8
print(pow(9, 0.5))  # Square root → 3.0
print("")
# Using math module
import math

print(math.floor(3.7))   # → 3 Ibababa (round down) ang number sa pinakamalapit na mas mababang integer.
print(math.ceil(3.7))    # → 4 Itataas (round up) ang number sa pinakamalapit na mas mataas na integer.
print(math.sqrt(64))     # → 8.0 Kukunin ang square root ng number.

